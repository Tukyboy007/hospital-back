-- Refresh tokens (rotation + revoke)
CREATE TABLE IF NOT EXISTS public.refresh_tokens (
    id BIGSERIAL PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES public.doctor_user(id) ON DELETE CASCADE,
    jti TEXT UNIQUE NOT NULL,
    token_hash TEXT NOT NULL,
    expires_at TIMESTAMPTZ NOT NULL,
    revoked BOOLEAN NOT NULL DEFAULT false,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);



-- (Хүсвэл зөвхөн бодит өөрчлөлт орсон үед л шинэчлэх хувилбар)
 CREATE OR REPLACE FUNCTION public.set_updated_at_if_changed()
 RETURNS TRIGGER AS $$
 BEGIN
   IF (to_jsonb(NEW) - 'updated_at') IS DISTINCT FROM (to_jsonb(OLD) - 'updated_at') THEN
     NEW.updated_at := NOW();
   END IF;
   RETURN NEW;
 END;
 $$ LANGUAGE plpgsql;

-- 2) Триггерүүд: updated_at баганатай хүснэгтүүдэд холбож байна
DROP TRIGGER IF EXISTS trg_doctor_user_updated_at ON public.doctor_user;
CREATE TRIGGER trg_doctor_user_updated_at
BEFORE UPDATE ON public.doctor_user
FOR EACH ROW EXECUTE FUNCTION public.set_updated_at_if_changed();

