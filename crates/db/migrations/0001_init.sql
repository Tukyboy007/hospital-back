-- ───────────────────────────────────────────────
-- updated_at trigger function + triggers
-- ───────────────────────────────────────────────
CREATE OR REPLACE FUNCTION public.set_updated_at_if_changed()
RETURNS TRIGGER AS $func$
BEGIN
  IF (to_jsonb(NEW) - 'updated_at') IS DISTINCT FROM (to_jsonb(OLD) - 'updated_at') THEN
    NEW.updated_at := NOW();
  END IF;
  RETURN NEW;
END;
$func$ LANGUAGE plpgsql;

-- triggers идемпотент
DROP TRIGGER IF EXISTS trg_doctor_user_updated_at ON public.doctor_user;
CREATE TRIGGER trg_doctor_user_updated_at
BEFORE UPDATE ON public.doctor_user
FOR EACH ROW EXECUTE FUNCTION public.set_updated_at_if_changed();

