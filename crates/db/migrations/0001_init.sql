-- ───────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.doctor_rolls (
    roll_id    INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    roll_name  TEXT NOT NULL UNIQUE
        CHECK (roll_name IN ('admin','senior','junior','viewer'))
);

INSERT INTO public.doctor_rolls (roll_name)
VALUES ('admin'),('senior'),('junior'),('viewer')
ON CONFLICT (roll_name) DO NOTHING;

-- ───────────────────────────────────────────────
-- doctor_user
-- ───────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.doctor_user (
    id             UUID  PRIMARY KEY DEFAULT gen_random_uuid(),
    doctor_id      SERIAL NOT NULL UNIQUE,       -- INT unique (FK target)
    rank_name      TEXT,
    first_name     TEXT,
    last_name      TEXT,
    org_name       TEXT,
    org_id         INT  NOT NULL,
    reg_no         TEXT NOT NULL UNIQUE,
    position       TEXT,
    birth_date     DATE,
    gender         TEXT,
    doctor_roll    INT  NOT NULL
                   REFERENCES public.doctor_rolls(roll_id)
                   ON UPDATE CASCADE ON DELETE RESTRICT,
    created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    password_hash  TEXT NOT NULL,
    is_active      BOOLEAN NOT NULL DEFAULT FALSE,
    CONSTRAINT chk_doctor_roll CHECK (doctor_roll IN (1,2,3,4))
);

